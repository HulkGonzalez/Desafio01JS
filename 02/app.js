//Solicitud de datos

//Se pide primer numero a cliente
let num01 = parseInt(prompt("Ingrese primer numero"));

//Se pide segundo numero a cliente
let num02 = parseInt(prompt("Ingrese segundo numero"));

//Resultados a opreaciones matematicas

//Resultado suma
let suma = num01 + num02;

//Resultado resta
let resta = num01 - num02;

//Resultado multiplicacion
let multiplicacion = num01 * num02;

//Resultado division
let division = num01 / num02;

//Resultado resto
let modulo = num01 % num02;

//Vista por consola 

console.log(`Resultado de operaciones matematicas:
  Suma: ${suma}
  Resta: ${resta}
  Multiplicacion: ${multiplicacion} 
  Division: ${division}
  Resto: ${modulo}
  `);
